# Generate init_embedded.py
generate_embedded_init() {
    print_info "Generating embedded dashboard initialization script..."

    cat > "$EMBEDDED_INIT" << 'EOF'
#!/usr/bin/env python3
"""
Initialize embedded dashboards for guest access.

This script:
1. Creates a 'Public' role with read-only access
2. Creates a 'guest' user with Public role
3. Enables embedded access for specified dashboards
4. Configures permissions for guest access

Usage:
    python init_embedded.py [dashboard_id1] [dashboard_id2] ...

    If no dashboard IDs provided, enables all dashboards for embedding.
"""

import sys
import logging
from superset import app, db, security_manager
from superset.models.dashboard import Dashboard
from superset.models.slice import Slice
from flask_appbuilder.security.sqla.models import Role, User

logging.basicConfig(level=logging.INFO)
logger = logging.getLogger(__name__)


def create_public_role():
    """Create Public role with read-only permissions."""
    logger.info("Creating/updating Public role...")

    # Get or create Public role
    public_role = security_manager.find_role('Public')

    if not public_role:
        public_role = security_manager.add_role('Public')
        logger.info("Created new Public role")

    # Define permissions for Public role
    permissions = [
        # Dashboard permissions
        ('can_read', 'Dashboard'),
        ('can_dashboard', 'Superset'),

        # Chart permissions
        ('can_read', 'Chart'),
        ('can_explore_json', 'Superset'),

        # Dataset permissions
        ('can_read', 'Dataset'),
        ('can_get', 'Datasource'),

        # Database permissions
        ('can_read', 'Database'),

        # Query permissions
        ('can_read', 'Query'),

        # Embedded permissions
        ('can_read', 'EmbeddedDashboard'),
    ]

    # Add permissions to role
    for permission_name, view_name in permissions:
        perm_view = security_manager.find_permission_view_menu(permission_name, view_name)
        if perm_view and perm_view not in public_role.permissions:
            public_role.permissions.append(perm_view)

    db.session.commit()
    logger.info("Public role configured with permissions")

    return public_role


def create_guest_user(public_role):
    """Create guest user with Public role."""
    logger.info("Creating/updating guest user...")

    # Check if guest user exists
    guest_user = security_manager.find_user('guest')

    if not guest_user:
        guest_user = security_manager.add_user(
            username='guest',
            first_name='Guest',
            last_name='User',
            email='guest@localhost',
            role=public_role,
            password='guest'  # Will be changed to a secure password
        )
        logger.info("Created new guest user")
    else:
        # Update role if needed
        if public_role not in guest_user.roles:
            guest_user.roles.append(public_role)
            db.session.commit()
            logger.info("Updated guest user role")

    return guest_user


def enable_embedded_dashboards(dashboard_ids=None):
    """Enable embedded access for dashboards.

    Args:
        dashboard_ids: List of dashboard IDs to enable. If None, enables all dashboards.
    """
    logger.info("Enabling embedded access for dashboards...")

    if dashboard_ids:
        dashboards = Dashboard.query.filter(Dashboard.id.in_(dashboard_ids)).all()
    else:
        dashboards = Dashboard.query.all()

    if not dashboards:
        logger.warning("No dashboards found")
        return

    enabled_count = 0
    for dashboard in dashboards:
        if not dashboard.embedded:
            dashboard.embedded = []

        # Enable embedded access
        if 'allow_domain_list' not in dashboard.embedded:
            dashboard.embedded.append({
                'allow_domain_list': ['*'],  # Allow all domains (configure in superset_config.py)
            })
            enabled_count += 1
            logger.info(f"Enabled embedding for dashboard: {dashboard.dashboard_title} (ID: {dashboard.id})")

    db.session.commit()
    logger.info(f"Enabled embedding for {enabled_count} dashboards")


def grant_dashboard_permissions(public_role):
    """Grant Public role access to all dashboards and charts."""
    logger.info("Granting dashboard and chart permissions to Public role...")

    # Get all dashboards
    dashboards = Dashboard.query.all()

    for dashboard in dashboards:
        # Create permission for this dashboard
        perm_view = security_manager.find_permission_view_menu(
            'can_read',
            f'[Dashboard].(id:{dashboard.id})'
        )

        if perm_view and perm_view not in public_role.permissions:
            public_role.permissions.append(perm_view)

    # Get all charts
    charts = Slice.query.all()

    for chart in charts:
        # Create permission for this chart
        perm_view = security_manager.find_permission_view_menu(
            'can_read',
            f'[Chart].(id:{chart.id})'
        )

        if perm_view and perm_view not in public_role.permissions:
            public_role.permissions.append(perm_view)

    db.session.commit()
    logger.info(f"Granted permissions for {len(dashboards)} dashboards and {len(charts)} charts")


def main():
    """Main initialization function."""
    logger.info("=" * 80)
    logger.info("Starting embedded dashboard initialization")
    logger.info("=" * 80)

    with app.app_context():
        # Get dashboard IDs from command line
        dashboard_ids = None
        if len(sys.argv) > 1:
            try:
                dashboard_ids = [int(id_str) for id_str in sys.argv[1:]]
                logger.info(f"Will enable embedding for specific dashboards: {dashboard_ids}")
            except ValueError:
                logger.error("Invalid dashboard IDs provided. Use integers only.")
                sys.exit(1)

        # Step 1: Create Public role
        public_role = create_public_role()

        # Step 2: Create guest user
        guest_user = create_guest_user(public_role)

        # Step 3: Enable embedded access for dashboards
        enable_embedded_dashboards(dashboard_ids)

        # Step 4: Grant permissions
        grant_dashboard_permissions(public_role)

        logger.info("=" * 80)
        logger.info("Embedded dashboard initialization complete!")
        logger.info("=" * 80)
        logger.info(f"Guest user: guest")
        logger.info(f"Public role: Public")
        logger.info("Next steps:")
        logger.info("1. Update GUEST_TOKEN_JWT_SECRET in superset_config.py")
        logger.info("2. Configure EMBEDDED_ALLOWED_DOMAINS in superset_config.py")
        logger.info("3. Restart Superset")


if __name__ == '__main__':
    main()
EOF

    chmod +x "$EMBEDDED_INIT"
    print_success "Embedded initialization script generated: $EMBEDDED_INIT"
}

# Initialize Superset database
init_database() {
    print_info "Initializing Superset database..."

    source "$VENV_DIR/bin/activate"

    # Set environment variables
    export SUPERSET_CONFIG_PATH="$CONFIG_FILE"
    export FLASK_APP=superset

    # Initialize database
    superset db upgrade

    # Create default roles and permissions
    superset init

    print_success "Database initialized"
}

# Create admin user
create_admin() {
    print_info "Creating admin user..."

    source "$VENV_DIR/bin/activate"
    export SUPERSET_CONFIG_PATH="$CONFIG_FILE"
    export FLASK_APP=superset

    # Check if admin exists
    if superset fab list-users | grep -q "admin"; then
        print_info "Admin user already exists"
    else
        # Create admin user interactively
        print_info "Please provide admin user details:"
        superset fab create-admin
        print_success "Admin user created"
    fi
}

# Initialize embedded dashboards
init_embedded() {
    print_info "Initializing embedded dashboards..."

    source "$VENV_DIR/bin/activate"
    export SUPERSET_CONFIG_PATH="$CONFIG_FILE"

    python "$EMBEDDED_INIT"

    print_success "Embedded dashboards initialized"
}

# Start Superset services
start_services() {
    print_info "Starting Superset services..."

    source "$VENV_DIR/bin/activate"
    export SUPERSET_CONFIG_PATH="$CONFIG_FILE"
    export FLASK_APP=superset

    # Use superset-manager.sh if it exists
    if [ -f "${SCRIPT_DIR}/superset-manager.sh" ]; then
        "${SCRIPT_DIR}/superset-manager.sh" start
    else
        # Fallback to direct superset run
        nohup superset run -h 0.0.0.0 -p 8088 --with-threads > "$LOG_DIR/superset.log" 2>&1 &
        echo $! > "$DATA_DIR/superset.pid"
    fi

    print_success "Superset services started"
    print_info "Access Superset at: http://localhost:8088"
}

# Stop Superset services
stop_services() {
    print_info "Stopping Superset services..."

    # Use superset-manager.sh if it exists
    if [ -f "${SCRIPT_DIR}/superset-manager.sh" ]; then
        "${SCRIPT_DIR}/superset-manager.sh" stop
    else
        # Fallback to PID file
        if [ -f "$DATA_DIR/superset.pid" ]; then
            kill $(cat "$DATA_DIR/superset.pid") 2>/dev/null || true
            rm "$DATA_DIR/superset.pid"
        fi
    fi

    print_success "Superset services stopped"
}

# Restart services
restart_services() {
    stop_services
    sleep 2
    start_services
}

# Check service status
check_status() {
    print_info "Checking Superset status..."

    # Use superset-manager.sh if it exists
    if [ -f "${SCRIPT_DIR}/superset-manager.sh" ]; then
        "${SCRIPT_DIR}/superset-manager.sh" status
    else
        if [ -f "$DATA_DIR/superset.pid" ] && kill -0 $(cat "$DATA_DIR/superset.pid") 2>/dev/null; then
            print_success "Superset is running (PID: $(cat $DATA_DIR/superset.pid))"
        else
            print_warning "Superset is not running"
        fi
    fi
}

# Upgrade database
upgrade_database() {
    print_info "Upgrading database..."

    source "$VENV_DIR/bin/activate"
    export SUPERSET_CONFIG_PATH="$CONFIG_FILE"
    export FLASK_APP=superset

    superset db upgrade

    print_success "Database upgraded"
}

# Clear DHIS2 cache
clear_cache() {
    print_info "Clearing DHIS2 cache..."

    source "$VENV_DIR/bin/activate"
    export SUPERSET_CONFIG_PATH="$CONFIG_FILE"

    python << 'PYTHON_SCRIPT'
from superset.utils.dhis2_cache import get_dhis2_cache

cache = get_dhis2_cache()
count = cache.clear()
print(f"Cleared {count} cache entries")

stats = cache.stats()
print(f"Cache stats: {stats}")
PYTHON_SCRIPT

    print_success "DHIS2 cache cleared"
}

# Full installation
full_install() {
    print_info "========================================="
    print_info "Starting full Superset installation"
    print_info "========================================="

    check_not_root
    check_prerequisites
    setup_venv
    install_dependencies
    create_directories
    generate_config
    generate_embedded_init
    init_database
    create_admin

    print_success "========================================="
    print_success "Installation complete!"
    print_success "========================================="
    print_info "Next steps:"
    print_info "1. Edit $CONFIG_FILE and set SECRET_KEY and GUEST_TOKEN_JWT_SECRET"
    print_info "2. Run: ./deploy.sh start"
    print_info "3. (Optional) Run: ./deploy.sh init-embedded"
}

# Show usage
show_usage() {
    cat << EOF
Usage: $0 {command}

Commands:
    install         - Full installation (first time only)
    start           - Start Superset services
    stop            - Stop Superset services
    restart         - Restart Superset services
    status          - Check service status
    upgrade         - Upgrade database schema
    clear-cache     - Clear DHIS2 cache
    init-embedded   - Initialize embedded dashboards
    help            - Show this help message

Examples:
    $0 install              # First-time installation
    $0 start                # Start services
    $0 init-embedded 1 2 3  # Enable embedding for dashboards 1, 2, 3
    $0 clear-cache          # Clear DHIS2 cache

EOF
}

# Main command dispatcher
main() {
    case "${1:-}" in
        install)
            full_install
            ;;
        start)
            start_services
            ;;
        stop)
            stop_services
            ;;
        restart)
            restart_services
            ;;
        status)
            check_status
            ;;
        upgrade)
            upgrade_database
            ;;
        clear-cache)
            clear_cache
            ;;
        init-embedded)
            shift
            init_embedded "$@"
            ;;
        help|--help|-h)
            show_usage
            ;;
        *)
            print_error "Unknown command: ${1:-}"
            show_usage
            exit 1
            ;;
    esac
}

# Run main function
main "$@"
