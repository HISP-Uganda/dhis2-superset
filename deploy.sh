set -e  # Exit on error

# Colors for output
RED='\033[0;31m'
GREEN='\033[0;32m'
YELLOW='\033[1;33m'
BLUE='\033[0;34m'
NC='\033[0m' # No Color

# Deployment configuration
SCRIPT_DIR="$(cd "$(dirname "${BASH_SOURCE[0]}")" && pwd)"
VENV_DIR="${SCRIPT_DIR}/venv"
LOG_DIR="${SCRIPT_DIR}/logs"
DATA_DIR="${SCRIPT_DIR}/data"
CONFIG_FILE="${SCRIPT_DIR}/superset_config.py"
EMBEDDED_INIT="${SCRIPT_DIR}/init_embedded.py"

# Print colored message
print_msg() {
    local color=$1
    shift
    echo -e "${color}[$(date +'%Y-%m-%d %H:%M:%S')] $@${NC}"
}

print_info() { print_msg "$BLUE" "ℹ️  $@"; }
print_success() { print_msg "$GREEN" "✅ $@"; }
print_warning() { print_msg "$YELLOW" "⚠️  $@"; }
print_error() { print_msg "$RED" "❌ $@"; }

# Check if running as root
check_not_root() {
    if [ "$EUID" -eq 0 ]; then
        print_error "Please do not run this script as root"
        exit 1
    fi
}

# Check prerequisites
check_prerequisites() {
    print_info "Checking prerequisites..."

    local missing_deps=()

    # Check Python
    if ! command -v python3 &> /dev/null; then
        missing_deps+=("python3")
    fi

    # Check pip
    if ! command -v pip3 &> /dev/null; then
        missing_deps+=("python3-pip")
    fi

    # Check Node.js (for frontend)
    if ! command -v node &> /dev/null; then
        print_warning "Node.js not found - frontend build will be skipped"
    fi

    if [ ${#missing_deps[@]} -gt 0 ]; then
        print_error "Missing dependencies: ${missing_deps[*]}"
        print_info "Install them with: sudo apt-get install ${missing_deps[*]}"
        exit 1
    fi

    print_success "Prerequisites check passed"
}

# Setup Python virtual environment
setup_venv() {
    print_info "Setting up Python virtual environment..."

    if [ ! -d "$VENV_DIR" ]; then
        python3 -m venv "$VENV_DIR"
        print_success "Virtual environment created"
    else
        print_info "Virtual environment already exists"
    fi

    # Activate virtual environment
    source "$VENV_DIR/bin/activate"

    # Upgrade pip
    pip install --upgrade pip

    print_success "Virtual environment ready"
}

# Install Python dependencies
install_dependencies() {
    print_info "Installing Python dependencies..."

    source "$VENV_DIR/bin/activate"

    # Install Superset and dependencies
    pip install apache-superset

    # Install DHIS2-specific dependencies
    pip install psycopg2-binary  # PostgreSQL support
    pip install gevent           # Async support

    print_success "Dependencies installed"
}

# Create directory structure
create_directories() {
    print_info "Creating directory structure..."

    mkdir -p "$LOG_DIR"
    mkdir -p "$DATA_DIR"
    mkdir -p "${SCRIPT_DIR}/static/assets"

    print_success "Directories created"
}

